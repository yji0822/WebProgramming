
--  SEQUENCE & TABLE DROP/CREATE
-- DROP
DROP TABLE BOOK CASCADE CONSTRAINTS;
DROP TABLE MEMBER CASCADE CONSTRAINTS;
DROP SEQUENCE BOOK_SQ;

-- CREATE
CREATE TABLE MEMBER (
    mID VARCHAR2(100) PRIMARY KEY,
    mPW VARCHAR2(100) NOT NULL,
    mNAME VARCHAR2(100) NOT NULL, -- 빈스트링을 넣으면 데이터에 값이 안들어간다. REQUEIRED=REQUIRED 필수
    mEMAIL VARCHAR2(100) NOT NULL,
    mPOST VARCHAR2(100), -- 우편번호
    mADDR VARCHAR2(100) -- 주소
);

CREATE SEQUENCE BOOK_SQ MAXVALUE 99999999 NOCYCLE NOCACHE;

CREATE TABLE BOOK(
    bNUM NUMBER(8) PRIMARY KEY,
    bTITLE VARCHAR2(100) NOT NULL,
    bWRITER VARCHAR2(100) NOT NULL,
    bRDATE DATE DEFAULT SYSDATE NOT NULL, -- 입력하면 TYPE=DATE 입력 안되면 TEMP BRDATE 
    bIMG1 VARCHAR2(1000) DEFAULT 'noImg.png' NOT NULL, -- 데이터베이스에서도 DEFAULT 이미지 넣어줄 수 있다.
    bIMG2 VARCHAR2(1000) DEFAULT 'noImg.png' NOT NULL,
    bINFO VARCHAR2(4000)
);


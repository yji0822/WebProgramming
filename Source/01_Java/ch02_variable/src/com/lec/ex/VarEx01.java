package com.lec.ex;

public class VarEx01 { 

	public static void main(String[] args) {
		int age = 28;
		String name = "배수지";
		// 데이터타입 변수명 = (할당연산자) 값;
		System.out.println("안녕하세요 " + age + "살 " + name + "입니다.");
		
		age = 30;
		name = "아이유";
		System.out.println("안녕하세요 " + age + "살 " + name + "입니다.");
		
		System.out.println();
		System.out.println("안녕하세요 28살 배수지 입니다.");
		System.out.println("안녕하세요 28살 배수지 입니다.");
		System.out.println("안녕하세요 28살 배수지 입니다.");

	}

}
